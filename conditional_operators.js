// IF-ELSE
const a = 3

if( a > 2) {
  console.log("pehla wala");
} else if( a < 2) {
  console.log("dusra wala");
} else {
  console.log("equal wala")
}

// SWITCH-CASE

let fruit = 'Apple';

switch(fruit) {
  case 'Mango':
    console.log("Mango is fruit");
    break;
  case "Apple":
    console.log("Apple is fruit");
    break;
  default:
    console.log("DEFAULT!!");
}

